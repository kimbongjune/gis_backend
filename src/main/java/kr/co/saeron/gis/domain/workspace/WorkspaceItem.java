package kr.co.saeron.gis.domain.workspace;

public class WorkspaceItem {
    private String name;
    private String href;

    public String getName() { 
    	return name; 
    }
    public void setName(String name) { 
    	this.name = name; 
	}
    public String getHref() { 
    	return href; 
	}
    public void setHref(String href) { 
    	this.href = href; 
	}
}
